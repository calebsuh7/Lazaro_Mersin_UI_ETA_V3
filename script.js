let polyline = L.polyline(currentRoute, { color: 'blue' }).addTo(map);
map.fitBounds(polyline.getBounds());

function interpolatePosition(p1, p2, t) {
  return [
    p1[0] + (p2[0] - p1[0]) * t,
    p1[1] + (p2[1] - p1[1]) * t
  ];
}

function animateSegment(start, end, duration, onDone) {
  const startTime = performance.now();
  function frame(now) {
    const elapsed = now - startTime;
    const t = Math.min(1, elapsed / duration);
    const pos = interpolatePosition(start, end, t);
    ship.setLatLng(pos);
    if (t < 1) requestAnimationFrame(frame);
    else onDone();
  }
  requestAnimationFrame(frame);
}

function startShip() {
  if (moving) return;
  moving = true;
  function moveNext() {
    if (!moving || currentIndex >= currentRoute.length - 1) {
      stopShip();
      return;
    }
    const from = currentRoute[currentIndex];
    const to = currentRoute[currentIndex + 1];
    animateSegment(from, to, 34, () => {
      currentIndex++;
      moveNext();
    });
  }
  moveNext();
}

function stopShip() {
  moving = false;
}

function setDirection(dir) {
  stopShip();
  currentRoute = dir === 'forward' ? forwardRoute : backwardRoute;
  currentIndex = 0;
  if (polyline) map.removeLayer(polyline);
  polyline = L.polyline(currentRoute, { color: 'blue' }).addTo(map);
  map.fitBounds(polyline.getBounds());
  ship.setLatLng(currentRoute[0]);
}



